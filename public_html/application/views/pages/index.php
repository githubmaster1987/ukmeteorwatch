    <div class="col-md-6">
        <h4>What is UKMON?</h4>
        <p>UKMON is a collaboration of amateur  astronomers who share a common interest in meteors.&nbsp; Together we are  building a network of CCTV cameras across the UK to monitor CCTV activity in  the night skies and capable of collecting valuable scientific data.</p>
      <h4>UKMON is all about  teamwork</h4>
      <p>We can learn only a little about any one  meteor from a single CCTV camera view, but by combining views from different  cameras we can determine altitudes, velocities, and even look for the origins  of a meteor.&nbsp; Do this for many thousands of meteors we are able to build a  very detailed picture.&nbsp; We can only do this by working together and  sharing our data.</p>
      <h4>Why we do it?</h4>
      <p>For lots or reasons, but for many of us it  is the opportunity participate in real science that motivates us.&nbsp; The  data gathered by UKMON partners contributes to our understanding of meteors and  is of interest to the professional scientific community.&nbsp; It&rsquo;s great fun  too as you never know what you're going to catch on the camera- it&rsquo;s rather  like astro-fishing.</p>
</div>

    <div class="col-md-6">
        <h4>Everyone is welcome</h4>
        <p>Anyone is welcome to join our network,  whether you are an amateur astronomer or a professional scientist, an astronomy  group or society, a school or &nbsp;colleges.&nbsp;&nbsp; Astronomy is your  interest as well as ours.</p>
      <h4>How to join in?</h4>
      <p>It's easy. See our UKMON website, follow us  on Twitter or facebook. Or if you want to be hands-on and set up your own CCTV  camera, just <a href="mailto:ukmeteornetwork@gmail.com">email us</a>.</p>
      <h4>What do I need?</h4>
      <p>We have published a simple guide which  tells you what is involved in setting up and running a CCTV meteor monitoring  station on the <a href="http://ukmeteornetwork.co.uk/" target="_blank">UK Meteor Network</a> website.&nbsp; </p>
      <p><a href="mailto:ukmeteornetwork@gmail.com" class="btn btn-default btn-small">Join UKMON network</a></p>
</div>

